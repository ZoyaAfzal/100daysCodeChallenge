//Question 119: Create a while loop that logs "Hello, World!" 5 times.

//Initializing a counter:
let count:number = 0;
//This while loop runs until count will reaches 5:
while(count < 5){
    console.log("Hello World!");
    count++; //Increments count by 1
}
//It will repeatedly log "Hello World!" 5 times, once per loop iteration.