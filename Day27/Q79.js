/*Question 79: Creating and Accessing Object Properties: Construct an object representing a car with properties
for make, model, and year. Then, show how you can access the model property of the car.*/
//This sets up an Object for a car with specific details:
let car = {
    make: "Ferrari",
    model: "SF90 Stradale",
    year: 2019,
};
//Accessing and showing the car make:
console.log(car.make);
//Accessing and showing the car model:
console.log(car.model);
//Accessing and sharing the car year:
console.log(car.year);
export {};
//We use dot notation to get the property of an Object.
