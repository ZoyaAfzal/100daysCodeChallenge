/*Question 82: Find the Length of a String: Write a function that takes a string as an argument and returns the 
number of characters in the string.*/

//This function counts how many characters are there in a string:
function stringLength(str:string):number {
    return str.length;
    //It will return the number of characters in the string.
}
//Example: Measuring the length of a name:
console.log(stringLength("Sophie"));
//It will tell us how long the string is now, and it comntains 4 characters.
