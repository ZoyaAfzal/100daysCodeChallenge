/*Question 73: Assigning and Updating Variables: Create a function where you declare a variable using let and assign an
initial value. Then, update its value within the same function and log both the initial and updated values.*/
function updateVariable() {
    // Declaring a variable using let and assign an initial value:
    let number = 10;
    //logging the initial value:
    console.log("Initial value:", number);
    //updating the value:
    number = 20;
    //logging the updated value
    console.log("Updated value:", number);
}
//calling the function
updateVariable();
//we assigned a value to a variable and themn updated it, showing how values cam change.
